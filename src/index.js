const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {

    let sym = '';
    while (expr.length > 0){
        let s0 = expr.substr(0,10);
        if (s0 == '**********'){
            sym += ' ';
        }            
        let c = '';
        for (let i=0; i<10; i+=2){
            if (s0[i] + s0[i+1] == '10'){
                c += '.';
            } else if (s0[i] + s0[i+1] == '11'){
                c += '-';
            } else if (s0[i] + s0[i+1] == '00'){
                c += '';
            }
            
        }
        let d = MORSE_TABLE[c];
        if (d != undefined){
            sym += d;
        }        
        expr = expr.substr(10);
    }
    return sym;
}

module.exports = {
    decode
}